.MODEL TINY
.DATA

    ATTRIBUTE DB 04H
	SROW      DW 80
	SCOL      DW 70
	LROW      DW 180
	LCOL      DW 150
	ORIGINAL   DB ?
.CODE
.STARTUP
  
   MOV AH,0FH
   INT 10H
  
   MOV ORIGINAL ,AL
   MOV AL,12H  ;GRAPHICAL MODEL
   MOV AH,0
   INT 10H
   
   MOV DX,SROW
X1:MOV CX,SCOL
 
X2: MOV AL,ATTRIBUTE
    MOV AH,0CH  ;FILLING A PIXEL
	INT 10H
	
	INC CX
	CMP CX,LCOL
	JNZ X2
	
	INC DX
	CMP DX,LROW
	JNZ X1 
X3: MOV AH,07H
    INT 21H
	CMP AL,'E'
	JZ X3
	
	MOV AL ,ORIGINAL
	MOV AH,0
	INT 10H
	
.EXIT
END
	