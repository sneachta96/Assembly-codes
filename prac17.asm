.MODEL TINY
.DATA
   DAT1 DB 5 DUP(?)
   CNT  DB 5
   CNT1 DB 5
.CODE
.STARTUP
    LEA SI,DAT1
	MOV CL,05
	
X1: MOV AH,08H
	INT 21H
    MOV [SI],AL
	INC SI
	DEC CL
	CMP CL,0
	JNZ X1
	
    MOV AH,0
	MOV AL,03H
	INT 10H
	
	LEA SI,DAT1
	MOV DL,12
	MOV DH,10
X2:	MOV BH,00
	MOV AH,02H
	INT 10H
	
	
	MOV BH,0
	MOV BL, 00001011B;
	MOV AH,09H
	MOV AL,[SI]
	MOV CX,1
	INT 10H
	 
	INC SI
	INC DH 
	DEC CNT
	CMP CNT,00
	JNZ X2
	
	
	LEA SI,DAT1
	MOV DH,12
	MOV DL,10
X3:	MOV BH,00
	MOV AH,02H
	INT 10H
	
	MOV BH,0
	MOV BL, 00001011B;
	MOV AH,09H
	MOV AL,[SI]
	MOV CX,1
	INT 10H
	 
	INC SI
	INC DL
	DEC CNT1
	CMP CNT1,00
	JNZ X3
	
	MOV AH,08H
X4:	INT 21H
	CMP AL,'?'
	JNZ X4
	
.EXIT
END
	
   