; CODE TO WRITE IN AN ALREADY EXISTING FILE CALLED FILE1.TXT


.MODEL TINY
.386
.DATA
  FNAME DB 'FILE1.txt',0
  HANDLE DW ?
  DATA DB 'HELLO WORLD'
.CODE
.STARTUP
  MOV AH,3DH
  MOV AL,2H
  LEA DX,FNAME
  INT 21H
  MOV BX,AX
  MOV HANDLE,BX
  MOV AH,40H
  MOV CX,11
  LEA DX,DATA
  INT 21H
  MOV AH,3EH
  MOV BX,HANDLE
  INT 21H
.EXIT
END
  